#정수형(int)
var1 = 1
var2 = 2
var3 = -3

print('var1 type :', type(var1))
print('var2 type :', type(var2))
print('var3 type :', type(var3))
print('var1 :', var1)
print('var2 :', var2)
print('var3 :', var3)

#실수형(float)
var4 = 0.1234567890
var5 = 0.12345678901234567890

print('var4 type :', type(var4))
print('var5 type :', type(var5))
print('var4 :', var4)
print('var5 :', var5)

#논리형(bool)
var6 = True
var7 = False

print('var6 type :', type(var6))
print('var7 type :', type(var7))
print('var6 = ', var6)
print('var7 = ', var7)

