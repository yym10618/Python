"""
산술연산자 예
"""

num1 = 100  # 피연산자1
num2 = 20   # 피연산자2

add = num1 + num2  # 덧셈
print('add=', add)

sub = num1 - num2  # 뺄셈
print('sub=', sub)

mul = num1 * num2  # 곱셈
print('mul=', mul)

div = num1 / num2  #나눗셈
print('div=', div)

div2 = num1 % num2 # 나머지계산
print('div2=', div2)

square = num1 ** 2 # 제곱계산
print('square=', square)
